#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	j;

	i = 0;
	if (ac == 2)
	{
		while (av[1][i] != '\0')
			i++;
		j = 0;
		while (av[1][j] != '\0')
		{
			write(1, &av[1][j], 1);
			if (i > 1)
			{
				write(1, "   ", 3);
				i--;
			}
			j++;
		}
		write(1, "\n", 1);
	}
	else
		write(1, "\n", 1);
}
